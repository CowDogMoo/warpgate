package main

import (
	"os"

	"github.com/spf13/cobra"
)

var completionCmd = &cobra.Command{
	Use:   "completion [bash|zsh|fish|powershell]",
	Short: "Generate shell completion script",
	Long: `Generate shell completion script for warpgate.

To load completions:

Bash:
  $ source <(warpgate completion bash)

  # To load completions for each session, execute once:
  # Linux:
  $ warpgate completion bash > /etc/bash_completion.d/warpgate
  # macOS:
  $ warpgate completion bash > $(brew --prefix)/etc/bash_completion.d/warpgate

Zsh:
  # If shell completion is not already enabled in your environment,
  # you will need to enable it.  You can execute the following once:
  $ echo "autoload -U compinit; compinit" >> ~/.zshrc

  # To load completions for each session, execute once:
  $ warpgate completion zsh > "${fpath[1]}/_warpgate"

  # You will need to start a new shell for this setup to take effect.

Fish:
  $ warpgate completion fish | source

  # To load completions for each session, execute once:
  $ warpgate completion fish > ~/.config/fish/completions/warpgate.fish

PowerShell:
  PS> warpgate completion powershell | Out-String | Invoke-Expression

  # To load completions for every new session, run:
  PS> warpgate completion powershell > warpgate.ps1
  # and source this file from your PowerShell profile.
`,
	DisableFlagsInUseLine: true,
	ValidArgs:             []string{"bash", "zsh", "fish", "powershell"},
	Args:                  cobra.MatchAll(cobra.ExactArgs(1), cobra.OnlyValidArgs),
	RunE: func(cmd *cobra.Command, args []string) error {
		switch args[0] {
		case "bash":
			return cmd.Root().GenBashCompletion(os.Stdout)
		case "zsh":
			return cmd.Root().GenZshCompletion(os.Stdout)
		case "fish":
			return cmd.Root().GenFishCompletion(os.Stdout, true)
		case "powershell":
			return cmd.Root().GenPowerShellCompletionWithDesc(os.Stdout)
		}
		return nil
	},
}
